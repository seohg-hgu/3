package project3;

public class DTO {

	String name;
	String s;
	String id;
	char[] pw;
	String phone;

	public String getName() {

		return name;

	}

	public void setName(String name) {

		this.name = name;

	}
	
	public String getS() {

		return s;

	}

	public void setS(String s) {

		this.s = s;

	}
	
	public String getId() {

		return id;

	}

	public void setId(String id) {

		this.id = id;

	}
	public char[] getPassword() {

		return pw;

	}

	public void setPassword(char[] pw) {

		this.pw = pw;

	}
	
	public String getPhone() {

		return phone;

	}

	public void setPhone(String phone) {

		this.phone = phone;

	}

}